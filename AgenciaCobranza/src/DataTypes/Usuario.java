
package DataTypes;


public class Usuario {

    protected String password;
    protected String user;

  
    public String getPassword() {
        return password;
    }

  
    public void setPassword(String value) {
        this.password = value;
    }

    
    public String getUser() {
        return user;
    }

   
    public void setUser(String value) {
        this.user = value;
    }

}
